export type TranslationKey = keyof typeof translations.en;

export const useTranslation = (lang: Language) => {
    return (key: TranslationKey | (string & {}), params?: Record<string, any>) => {
        let text = (translations[lang] as any)[key] || (translations.en as any)[key] || key;
        
        if (params) {
            Object.keys(params).forEach(p => {
                text = text.replace(`{{${p}}}`, params[p]);
            });
        }
        
        return text;
    };
};

export const translateDeviceName = (name: string, lang: Language) => {
    if (lang !== 'bg') return name;

    const rooms: Record<string, string> = {
        'Living Room': 'Всекидневна',
        'Kitchen': 'Кухня',
        'Basement': 'Мазе',
        'Bedroom': 'Спалня',
        'Garage': 'Гараж',
        'Bathroom': 'Баня'
    };

    const types: Record<string, string> = {
        'Air Quality': 'Качество на въздуха',
        'Humidity': 'Влажност',
        'Temperature': 'Температура',
        'Temperature Sensor': 'Сензор за температура',
        'Motion Sensor': 'Сензор за движение',
        'Motion': 'Сензор за движение',
        'Smoke Sensor': 'Сензор за дим',
        'Lights': 'Осветление',
        'Lighting': 'Осветление',
        'Air Conditioner': 'Климатик',
        'Air Purifier': 'Пречиствател за въздух',
        'Coffee Machine': 'Кафе машина',
        'Robot Vacuum': 'Прахосмукачка',
        'Dehumidifier': 'Влагоабсорбатор',
        'Vacuum': 'Прахосмукачка',
        'Fan': 'Вентилатор',
        'Nightstand': 'Нощно шкафче',
        'Right Window': 'Десен прозорец',
        'Left Window': 'Ляв прозорец',
        'Primary Bedroom Lights': 'Основно осветление - Спалня'
    };

    // 1. Exact match
    if (types[name]) return types[name];

    // 2. Specific case: "Left Window Motion Sensor"
    if (name === 'Left Window Motion Sensor') return 'Сензор за движение - Ляв прозорец';

    // 3. Room + Type pattern (e.g. "Basement Humidity")
    for (const [enRoom, bgRoom] of Object.entries(rooms)) {
        if (name.startsWith(enRoom)) {
            const rest = name.replace(enRoom, '').trim();
            if (types[rest]) return `${types[rest]} - ${bgRoom}`;
            
            // Handle numbers (e.g. "Lights 1")
            const match = rest.match(/^(.*?)\s*(\d+)$/);
            if (match && types[match[1]]) return `${types[match[1]]} ${match[2]} - ${bgRoom}`;
            
            return `${rest} - ${bgRoom}`;
        }
    }

    // 4. Type + Room pattern (e.g. "Dehumidifier Garage", "Fan Bedroom")
    for (const [enRoom, bgRoom] of Object.entries(rooms)) {
        if (name.endsWith(enRoom)) {
            const rest = name.replace(enRoom, '').trim();
            if (types[rest]) return `${types[rest]} - ${bgRoom}`;

            // Special case for reverse patterns "Fan Bedroom"
            if (rest === 'Fan') return `Вентилатор - ${bgRoom}`;
        }
    }

    // 5. General handle for Nightstand 1, etc.
    if (name.startsWith('Nightstand')) {
        return name.replace('Nightstand', 'Нощно шкафче');
    }

    return name;
};
