def main() -> None:
    with zipfile.ZipFile(SRC, "r") as zin:
        files = {name: zin.read(name) for name in zin.namelist()}

    doc = ET.fromstring(files["word/document.xml"])
    body = doc.find("w:body", NS)
    assert body is not None

    for p in body.findall("w:p", NS):
        text = para_text(p)
        if text in replacements:
            set_para_text(p, replacements[text])

    children = list(body)
    insertions: list[tuple[int, ET.Element]] = []
    for idx, child in enumerate(children):
        if child.tag != W + "p":
            continue
        text = para_text(child)
        if text == "- групиране на устройствата по помещения.":
            insertions.append(
                (
                    idx + 1,
                    make_para_like(
                        child,
                        "- интеграция с микроконтролер или други устройства, като осигурява обмен на данни и управление.",
                    ),
                )
            )
        if text == "- симулационна логика.":
            insertions.append(
                (
                    idx + 1,
                    make_para_like(
                        child,
                        "- интеграция с микроконтролер или други устройства, като осигурява обмен на данни и управление.",
                    ),
                )
            )
        if text == "- управление на потребители от настройките.":
            insertions.append(
                (
                    idx + 1,
                    make_para_like(
                        child,
                        "- базова интеграция с микроконтролер или други външни устройства за обмен на данни и управление.",
                    ),
                )
            )
        if text == "- симулирани данни при липса на реален хардуер.":
            insertions.append(
                (
                    idx + 1,
                    make_para_like(
                        child,
                        "- обмен на данни и управление при интеграция с външни устройства.",
                    ),
                )
            )

    for idx, newp in sorted(insertions, key=lambda x: x[0], reverse=True):
        body.insert(idx, newp)

    files["word/document.xml"] = ET.tostring(doc, encoding="utf-8", xml_declaration=True)

    with zipfile.ZipFile(OUT, "w", zipfile.ZIP_DEFLATED) as zout:
        for name, content in files.items():
            zout.writestr(name, content)

    print(OUT)


if __name__ == "__main__":
    main()
